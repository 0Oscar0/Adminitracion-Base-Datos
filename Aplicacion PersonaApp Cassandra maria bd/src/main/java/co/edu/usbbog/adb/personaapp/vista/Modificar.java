    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        String usuarioLogin=Login.jTextFieldUsuario.getText();
        if(pr.getSgdb().equals("maria")){
        Gestion ge =new Gestion();
        ge.ActualizarUsuario(jTextFieldNombreM.getText(), jTextFieldApellidoM.getText(), Integer.parseInt(jTextFieldEdadM.getText()), jTextFieldTel1.getText(),jTextFieldTel2.getText(), jTextFieldCorreo1M1.getText(), jTextFieldCorreo2M.getText(), jTextFieldGeneroM.getText(),usuarioLogin,jTextFieldContraseniaM.getText());
        
        }else if(pr.getSgdb().equals("cassandra")){
        UsuarioDao usuario = new UsuarioDao();
        usuario.EditarUsuario(jTextFieldNombreM.getText(), jTextFieldApellidoM.getText(), Integer.parseInt(jTextFieldEdadM.getText()), jTextFieldTel1.getText(),jTextFieldTel2.getText(), jTextFieldCorreo1M1.getText(), jTextFieldCorreo2M.getText(), jTextFieldGeneroM.getText(),usuarioLogin,jTextFieldContraseniaM.getText());
        }
        pr.deModificarAMenu(this);
        
    }//GEN-LAST:event_jButtonActualizarActionPerformed

