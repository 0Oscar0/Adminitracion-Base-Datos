    private void inicio() {
        if(validar()){
            pr.deLoginAMenu(this); 
            
        }else{
            JOptionPane.showMessageDialog(this, "Error");
            limpiar();
        }        
    }

    private boolean validar() {
        if (jTextFieldUsuario.getText().equals("")) {
            return false;
        } else if (jTextFieldContrasenia.getText().equals("")) {
            return false;
        } else if (jRadioButtoncassandra.isSelected()) {
            pr.setSgdb("cassandra");
            return g.iniciarSesion("cassandra", jTextFieldUsuario.getText(), jTextFieldContrasenia.getText());
        } else if (jRadioButtonmariadb.isSelected()) {
            pr.setSgdb("maria");
            return g.iniciarSesion("maria", jTextFieldUsuario.getText(), jTextFieldContrasenia.getText());
        } else {
            return false;
        }
    }

    private void limpiar() {
        jTextFieldUsuario.setText("");
        jTextFieldContrasenia.setText("");
    }


    
